rua = 'Avenida Marcelino de Almeida Fontoura, '
numero = '150, '
bairro = 'Sul, '
cidade = 'Rio de Janeiro, '
estado = 'RJ, '
cep = '22220-000'

#Concatenando strings
endereco = rua + numero + bairro + cidade + estado + cep
print(endereco)

#Para maiúsculas
endereco_maiscula = endereco.upper()
print(endereco_maiscula)

#Para minúsculas
endereco_minúsculas = endereco.lower()
print(endereco_minúsculas)