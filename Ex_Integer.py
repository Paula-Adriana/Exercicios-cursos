#Se nao for declarado como acima, a divisao de um integer/integer será float. 
divisao = 50/2
print(divisao) # 25.0
print(type(divisao)) # <class 'float'>

divisao = int(50/2)
print(divisao) # 25
print(type(divisao)) # <class 'int'>

a = 60
print(a)
print(type(a)) #irá retornar: <class 'int'> ou seja, do tipo inteiro. 

